#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*dst;
	t_list	*node;
	void	*content;

	dst = NULL;
	while (lst)
	{
		content = f(lst->content);
		node = ft_lstnew(content);
		if (!node)
		{
			del(content);
			ft_lstclear(&dst, del);
			return (NULL);
		}
		ft_lstadd_back(&dst, node);
		lst = lst->next;
	}
	return (dst);
}
